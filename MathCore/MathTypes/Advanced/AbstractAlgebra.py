# Polynomials over 𝔽ₚ: list [a₀,a₁,...,aₙ] where aᵢ is coefficient of xⁱ, all mod p

# Formula: (f+g) mod p
def PolyAdd(f, g, p):
    n=max(len(f),len(g)); r=[0]*n
    for i in range(len(f)): r[i]=(r[i]+f[i])%p
    for i in range(len(g)): r[i]=(r[i]+g[i])%p
    return _poly_trim(r)

# Formula: (f·g) mod p
def PolyMul(f, g, p):
    r=[0]*(len(f)+len(g)-1)
    for i,a in enumerate(f):
        for j,b in enumerate(g):
            r[i+j]=(r[i+j]+a*b)%p
    return _poly_trim(r)

# Formula: polynomial division f = q·g + r over 𝔽ₚ
# Returns (quotient, remainder)
def PolyDivMod(f, g, p):
    f=_poly_trim(list(f)); g=_poly_trim(list(g))
    if all(c==0 for c in g): raise ValueError('Division by zero polynomial')
    deg_g=_poly_degree(g); r=list(f); q=[0]*max(1, len(f))
    for i in range(len(f)-deg_g-1, -1, -1):
        if _poly_degree(r)<deg_g: break
        dr=_poly_degree(r)
        if dr<0: break
        coef=(r[dr]*FieldInverse(g[deg_g],p))%p
        q[dr-deg_g]=coef
        for j in range(deg_g+1):
            r[dr-deg_g+j]=(r[dr-deg_g+j]-coef*g[j])%p
    return _poly_trim(q), _poly_trim(r)

# Formula: gcd(f,g) in 𝔽ₚ[x] via Euclidean algorithm — returns monic GCD
def PolyGCD(f, g, p):
    f,g=list(f),list(g)
    while any(c!=0 for c in g):
        _,r=PolyDivMod(f,g,p); f,g=g,r
    f=_poly_trim(f)
    if f and f[-1]!=0:
        inv=FieldInverse(f[-1],p)
        f=[(c*inv)%p for c in f]
    return _poly_trim(f)

# Returns f(a) mod p
def PolyEval(f, a, p):
    return sum(f[i]*pow(int(a),i,p) for i in range(len(f)))%p

# Returns all roots of f in 𝔽ₚ
def PolyRoots(f, p):
    return [a for a in range(p) if PolyEval(f,a,p)==0]

# Formula: f is irreducible over 𝔽ₚ iff deg≥1 and f has no proper monic divisor
def IsIrreducible(f, p):
    f=_poly_trim(list(f)); deg=_poly_degree(f)
    if deg<=0: return False
    if deg==1: return True
    for a in range(p):                                         # Check for roots
        if PolyEval(f,a,p)==0: return False
    # Check all monic polynomials of degree 1..deg//2 for divisibility
    for d in range(2, deg//2+1):
        for coeffs in itertools.product(range(p), repeat=d):
            test=_poly_trim(list(coeffs)+[1])
            if _poly_degree(test)==d:
                _,r=PolyDivMod(f,test,p)
                if all(c==0 for c in r): return False          # Factor found
    return True

# Formula: (f·g) mod h mod p — multiplication in 𝔽ₚ[x]/⟨h⟩
def PolyMulMod(f, g, h, p):
    _,r=PolyDivMod(PolyMul(f,g,p), h, p); return r

