import math        # factorial, gcd, sqrt
import itertools   # product, permutations, combinations

# Groups are represented as (elements, op) where:
#   elements — a list of values (need not be hashable)
#   op       — a callable op(a, b) returning the group product a*b
# Rings/fields use (elements, add, mul) triples.
# Permutations are dicts {1: σ(1), 2: σ(2), ...} on {1,...,n}.
# Polynomials are lists [a₀, a₁, ..., aₙ] where index = degree, all mod p.

# ============================================================
# INTERNAL HELPERS  (prefixed _ — not part of public API)
# ============================================================

def _eq(a, b):
    """Element equality — works for both hashable and unhashable types (e.g. dicts)."""
    return a == b

def _in(x, lst):
    """List-based membership check (works when elements are not hashable)."""
    return any(_eq(x, y) for y in lst)

def _dedup(lst):
    """Remove duplicate elements from a list using equality."""
    result = []
    for x in lst:
        if not _in(x, result):
            result.append(x)
    return result

def _find_identity(elements, op):
    """Return the identity element, or None if not found."""
    for e in elements:
        if all(_eq(op(e,a),a) and _eq(op(a,e),a) for a in elements):
            return e
    return None

def _find_inverse(a, elements, op, identity):
    """Return b such that op(a,b) = op(b,a) = identity, or None."""
    for b in elements:
        if _eq(op(a,b), identity) and _eq(op(b,a), identity):
            return b
    return None

def _inverses_map(elements, op, identity):
    """Return list of (a, a_inv) pairs, or None if any element has no inverse."""
    pairs = []
    for a in elements:
        inv = _find_inverse(a, elements, op, identity)
        if inv is None:
            return None
        pairs.append((a, inv))
    return pairs

def _get_inv(a, pairs):
    """Retrieve inverse of a from the (element, inverse) pairs list."""
    for x, xi in pairs:
        if _eq(x, a):
            return xi
    return None

def _is_prime(n):
    if n < 2: return False
    for i in range(2, int(math.sqrt(n)) + 1):
        if n % i == 0: return False
    return True

def _poly_trim(poly):
    p = list(poly)
    while len(p) > 1 and p[-1] == 0:
        p.pop()
    return p

def _poly_degree(poly):
    p = _poly_trim(list(poly))
    return len(p) - 1 if any(c != 0 for c in p) else -1

