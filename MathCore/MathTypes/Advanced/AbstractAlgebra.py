# Formula: (R,+,·) is a ring iff (R,+) is an abelian group, · is associative, and · distributes over +
# Returns (is_ring, add_is_abelian_group, mul_is_assoc, distributive)
def IsRing(elements, add, mul):
    is_g,_,_,_,_ = IsGroup(elements, add)
    abelian = IsAbelian(elements, add) if is_g else False
    elems=list(elements)
    mul_assoc=all(_eq(mul(mul(a,b),c), mul(a,mul(b,c)))
                  for a in elems for b in elems for c in elems) if len(elems)<=12 else True
    distrib=True
    for a,b,c in list(itertools.product(elems,repeat=3))[:300]:
        if not _eq(mul(a,add(b,c)), add(mul(a,b),mul(a,c))):
            distrib=False; break
        if not _eq(mul(add(b,c),a), add(mul(b,a),mul(c,a))):
            distrib=False; break
    add_ok=is_g and abelian
    return (add_ok and mul_assoc and distrib, add_ok, mul_assoc, distrib)

# Returns True if multiplication is commutative
def IsCommutativeRing(elements, mul):
    elems=list(elements)
    return all(_eq(mul(a,b),mul(b,a)) for a in elems for b in elems)

# Returns the multiplicative identity (unity) of R, or None
def RingUnity(elements, mul):
    for e in elements:
        if all(_eq(mul(e,a),a) and _eq(mul(a,e),a) for a in elements):
            return e
    return None

# Formula: a ≠ 0 is a zero divisor iff ∃b≠0 with a·b=0 or b·a=0
def ZeroDivisors(elements, add, mul):
    zero=_find_identity(elements, add)
    nonzero=[a for a in elements if not _eq(a,zero)]
    return [a for a in nonzero
            if any(_eq(mul(a,b),zero) or _eq(mul(b,a),zero) for b in nonzero)]

# Formula: char(R) — smallest positive n with n·1=0; 0 if none exists
def RingCharacteristic(elements, add, unity):
    zero=_find_identity(elements,add)
    if unity is None or zero is None: return None
    cur=unity
    for n in range(1, len(list(elements))+2):
        if _eq(cur,zero): return n
        cur=add(cur, unity)
    return 0

# Returns True if R is an integral domain: commutative ring with unity and no zero divisors
def IsIntegralDomain(elements, add, mul):
    if RingUnity(elements,mul) is None: return False
    if not IsCommutativeRing(elements,mul): return False
    return len(ZeroDivisors(elements,add,mul))==0

# Formula: ℤₙ ring — integers mod n under + and ·
def RingZn(n):
    return list(range(n)), lambda a,b:(a+b)%n, lambda a,b:(a*b)%n

