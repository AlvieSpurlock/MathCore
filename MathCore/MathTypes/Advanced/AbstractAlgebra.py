# Formula: one-step subgroup test — H ≤ G iff ∀a,b ∈ H: ab⁻¹ ∈ H
def IsSubgroup(H, G_elements, op, identity):
    H = list(H); G = list(G_elements)
    if not H: return False
    if not all(_in(h, G) for h in H): return False
    pairs = _inverses_map(G, op, identity)
    if pairs is None: return False
    return all(_in(op(a, _get_inv(b, pairs)), H) for a in H for b in H)

# Formula: aH = {a∗h | h ∈ H} — left coset
def LeftCoset(a, H, op):
    return _dedup([op(a,h) for h in H])

# Formula: Ha = {h∗a | h ∈ H} — right coset
def RightCoset(H, a, op):
    return _dedup([op(h,a) for h in H])

# Formula: Lagrange's Theorem — |H| divides |G|, index [G:H] = |G|/|H|
def LagrangesTheorem(G_elements, H):
    g=len(list(G_elements)); h=len(list(H))
    if h==0: return None,False
    divides=g%h==0
    return (g//h if divides else None), divides

# Returns all left cosets of H in G as a list of lists
def AllLeftCosets(G_elements, H, op):
    G=list(G_elements); cosets=[]
    for g in G:
        coset=LeftCoset(g,H,op)
        if not any(set(map(str,coset))==set(map(str,c)) for c in cosets):
            cosets.append(coset)
    return cosets

# Formula: Z(G) = {z ∈ G | zg = gz for all g ∈ G} — center of G
def Center(G_elements, op):
    G=list(G_elements)
    return [z for z in G if all(_eq(op(z,g),op(g,z)) for g in G)]

