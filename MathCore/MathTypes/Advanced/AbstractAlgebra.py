# Formula: Orb(x) = {g·x | g ∈ G} — orbit of x
def Orbit(x, G_elements, action):
    return _dedup([action(g,x) for g in G_elements])

# Formula: Stab(x) = {g ∈ G | g·x = x} — stabilizer of x
def Stabilizer(x, G_elements, action):
    return [g for g in G_elements if _eq(action(g,x), x)]

# Formula: |Orb(x)| · |Stab(x)| = |G| — Orbit-Stabilizer Theorem
def OrbitStabilizerTheorem(x, G_elements, action):
    G=list(G_elements)
    orb=len(Orbit(x,G,action)); stab=len(Stabilizer(x,G,action))
    return orb, stab, orb*stab, len(G)

# Returns the partition of X into disjoint orbits
def AllOrbits(X, G_elements, action):
    X=list(X); G=list(G_elements); seen=[]; orbits=[]
    for x in X:
        if _in(x,seen): continue
        orb=Orbit(x,G,action)
        orbits.append(orb)
        for y in orb:
            if not _in(y,seen): seen.append(y)
    return orbits

# Formula: Burnside's Lemma — |X/G| = (1/|G|) Σ_{g∈G} |Fix(g)|
# Returns (number_of_orbits, {str(g): fix_count})
def BurnsideLemma(X, G_elements, action):
    G=list(G_elements); X_list=list(X)
    fixes={str(g): sum(1 for x in X_list if _eq(action(g,x),x)) for g in G}
    total=sum(fixes.values())
    return total//len(G) if total%len(G)==0 else total/len(G), fixes

# Formula: Cl(g) = {hgh⁻¹ | h ∈ G} — conjugacy class of g
def ConjugacyClass(g, G_elements, op, inverse_pairs):
    return _dedup([op(op(h,g), _get_inv(h,inverse_pairs)) for h in G_elements])

# Returns all conjugacy classes of G as a list of lists
def AllConjugacyClasses(G_elements, op, identity):
    G=list(G_elements); pairs=_inverses_map(G, op, identity)
    if pairs is None: return []
    seen=[]; classes=[]
    for g in G:
        if _in(g,seen): continue
        cls=ConjugacyClass(g, G, op, pairs)
        classes.append(cls)
        for x in cls:
            if not _in(x,seen): seen.append(x)
    return classes

# Formula: class equation — |G| = |Z(G)| + Σ [G:C_G(xᵢ)] for non-central xᵢ
# Returns (|G|, |Z(G)|, list_of_class_sizes)
def ClassEquation(G_elements, op, identity):
    G=list(G_elements)
    classes=AllConjugacyClasses(G, op, identity)
    # Elements in the center have singleton conjugacy classes
    center=[g for g in G
            if any(len(cls)==1 and _in(g,cls) for cls in classes)]
    sizes=[len(c) for c in classes]
    return len(G), len(center), sizes                          # Complete class equation

# Formula: Sylow p-subgroups — all subgroups of G of order p^k (brute force, small groups)
def SylowSubgroups(G_elements, p, k, op, identity):
    order=p**k; G=list(G_elements); result=[]
    for subset in itertools.combinations(G, order):
        S=list(subset)
        if _in(identity,S) and IsSubgroup(S,G,op,identity):
            if not any(set(map(str,S))==set(map(str,r)) for r in result):
                result.append(S)
    return result
