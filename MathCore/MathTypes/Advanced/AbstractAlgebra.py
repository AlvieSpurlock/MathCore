# Formula: φ: G→H is a homomorphism iff φ(ab) = φ(a)φ(b) for all a,b ∈ G
# phi is a dict mapping G-elements to H-elements
def IsHomomorphism(G_elements, H_elements, phi, G_op, H_op):
    G=list(G_elements)
    return all(_eq(phi.get(G_op(a,b)), H_op(phi.get(a), phi.get(b))) for a in G for b in G)

# Formula: ker(φ) = {g ∈ G | φ(g) = eH}
def Kernel(phi, H_identity):
    return {g for g,img in phi.items() if _eq(img, H_identity)}

# Formula: im(φ) = {φ(g) | g ∈ G}
def Image(phi):
    return set(phi.values())

# Formula: isomorphism iff bijective homomorphism
def IsIsomorphism(G_elements, H_elements, phi, G_op, H_op):
    G=list(G_elements); H=list(H_elements)
    if not IsHomomorphism(G,H,phi,G_op,H_op): return False
    if len(set(phi.values())) != len(G): return False          # Not injective
    if set(phi.values()) != set(H): return False               # Not surjective
    return True

# Formula: First Isomorphism Theorem — G/ker(φ) ≅ im(φ), |G|/|ker|=|im|
def FirstIsomorphismTheorem(G_elements, phi, G_op, H_identity):
    ker=Kernel(phi, H_identity); img=Image(phi)
    n=len(list(G_elements)); k=len(ker)
    return ker, img, n//k if k>0 and n%k==0 else None

