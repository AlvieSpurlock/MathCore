# Permutation on {1,...,n}: dict {1:σ(1), ..., n:σ(n)}

# Formula: (σ∘τ)(i) = σ(τ(i)) — apply τ first then σ
def PermCompose(sigma, tau):
    return {i: sigma[tau[i]] for i in tau}

# Formula: identity permutation on {1,...,n}
def PermIdentity(n):
    return {i: i for i in range(1, n+1)}

# Formula: σ⁻¹ — swap keys and values
def PermInverse(sigma):
    return {v: k for k,v in sigma.items()}

# Formula: cycle decomposition — σ as product of disjoint cycles (fixed points omitted)
def CycleDecomposition(sigma):
    visited=set(); cycles=[]
    for start in sorted(sigma):
        if start in visited: continue
        cycle=[]; x=start
        while x not in visited:
            visited.add(x); cycle.append(x); x=sigma[x]
        if len(cycle)>1: cycles.append(tuple(cycle))
    return cycles

# Formula: cycle type — sorted tuple of cycle lengths (including 1-cycles for fixed points)
def CycleType(sigma):
    n=len(sigma); cycles=CycleDecomposition(sigma)
    lens=sorted([len(c) for c in cycles], reverse=True)
    total=sum(lens)
    if total<n: lens+=[1]*(n-total)
    return tuple(sorted(lens, reverse=True))

# Formula: ord(σ) = lcm of all cycle lengths
def PermOrder(sigma):
    def lcm(a,b): return abs(a*b)//math.gcd(a,b)
    cycles=CycleDecomposition(sigma)
    if not cycles: return 1
    order=1
    for c in cycles: order=lcm(order, len(c))
    return order

# Formula: sgn(σ) = (−1)^(n − number of cycles including fixed points)
def PermSign(sigma):
    n=len(sigma)
    k=len(CycleDecomposition(sigma))+(n-sum(len(c) for c in CycleDecomposition(sigma)))
    return 1 if (n-k)%2==0 else -1

def IsEvenPermutation(sigma):
    return PermSign(sigma)==1

# Formula: Sₙ — all n! permutations on {1,...,n}
def SymmetricGroup(n):
    return [{i+1:p[i] for i in range(n)} for p in itertools.permutations(range(1,n+1))]

# Formula: Aₙ — all even permutations on {1,...,n}
def AlternatingGroup(n):
    return [s for s in SymmetricGroup(n) if IsEvenPermutation(s)]

# Formula: transposition (i j) on {1,...,n}
def Transposition(n, i, j):
    s=PermIdentity(n); s[i],s[j]=j,i; return s

# Returns σ expressed as a list of transpositions (each is a pair (a,b))
def ToTranspositions(sigma):
    trans=[]
    for cycle in CycleDecomposition(sigma):
        for k in range(len(cycle)-1):
            trans.append((cycle[0], cycle[k+1]))
    return trans

