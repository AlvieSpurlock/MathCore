# Formula: (F,+,·) is a field iff it is a commutative ring with unity where every nonzero element
# has a multiplicative inverse
def IsField(elements, add, mul):
    is_r,add_ok,mul_assoc,distrib=IsRing(elements,add,mul)
    comm=IsCommutativeRing(elements,mul) if is_r else False
    unity=RingUnity(elements,mul); has_u=unity is not None
    zero=_find_identity(elements,add); nonzero=[a for a in elements if not _eq(a,zero)]
    all_inv=has_u and all(any(_eq(mul(a,b),unity) for b in nonzero) for a in nonzero)
    return (is_r and comm and has_u and all_inv, is_r, comm, has_u, all_inv)

# Formula: 𝔽ₚ — integers mod p under + and · (p must be prime)
def PrimeField(p):
    if not _is_prime(p): raise ValueError(f'{p} is not prime')
    return list(range(p)), lambda a,b:(a+b)%p, lambda a,b:(a*b)%p

# Formula: a⁻¹ mod p via Fermat's little theorem: a^(p-2) mod p
def FieldInverse(a, p):
    if a%p==0: return None
    return pow(int(a), p-2, p)

# Formula: char(F) — characteristic of the field
def FieldCharacteristic(elements, add, mul):
    return RingCharacteristic(elements, add, RingUnity(elements,mul))

# Returns the prime subfield generated by the unity element
def PrimeSubfield(elements, add, mul):
    unity=RingUnity(elements,mul)
    if unity is None: return []
    zero=_find_identity(elements,add); sub=[zero]; cur=unity
    while not _in(cur,sub): sub.append(cur); cur=add(cur,unity)
    return sorted(sub)

