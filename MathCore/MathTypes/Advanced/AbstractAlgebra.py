# Formula: N ◁ G iff gNg⁻¹ = N for all g ∈ G
def IsNormalSubgroup(H, G_elements, op, identity):
    if not IsSubgroup(H, G_elements, op, identity): return False
    G=list(G_elements); pairs=_inverses_map(G, op, identity)
    if pairs is None: return False
    for g in G:
        g_inv=_get_inv(g, pairs)
        conj=_dedup([op(op(g,h),g_inv) for h in H])
        if set(map(str,conj)) != set(map(str,H)): return False
    return True

# Formula: G/N — quotient group whose elements are the left cosets of N
def QuotientGroup(G_elements, N, op, identity):
    if not IsNormalSubgroup(N, G_elements, op, identity): return None,None
    cosets=AllLeftCosets(G_elements, N, op)
    def coset_idx(x):
        for i,c in enumerate(cosets):
            if _in(x, c): return i
        return -1
    def coset_op(i,j):
        return coset_idx(op(cosets[i][0], cosets[j][0]))
    return cosets, coset_op

# Formula: [a,b] = a⁻¹b⁻¹ab — group commutator
def Commutator(a, b, op, inverse_pairs):
    a_inv=_get_inv(a, inverse_pairs); b_inv=_get_inv(b, inverse_pairs)
    return op(op(a_inv, b_inv), op(a,b))

# Formula: [G,G] = ⟨[a,b] | a,b ∈ G⟩ — commutator (derived) subgroup
def CommutatorSubgroup(G_elements, op, identity):
    G=list(G_elements); pairs=_inverses_map(G, op, identity)
    if pairs is None: return []
    comms=[]
    for a in G:
        for b in G:
            c=Commutator(a,b,op,pairs)
            if not _in(c, comms): comms.append(c)
    # Close under the group operation
    changed=True
    while changed:
        changed=False
        for c1 in list(comms):
            for c2 in list(comms):
                prod=op(c1,c2)
                if not _in(prod, comms): comms.append(prod); changed=True
    return comms                                               # Complete [G,G]

