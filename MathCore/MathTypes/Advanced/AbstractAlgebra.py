# Formula: I is a left ideal iff I is an additive subgroup and r·i ∈ I for all r ∈ R, i ∈ I
def IsLeftIdeal(I, R_elements, add, mul, add_identity):
    I=list(I); R=list(R_elements)
    pairs=_inverses_map(R, add, add_identity)
    if pairs is None: return False
    if not all(_in(add(a, _get_inv(b,pairs)), I) for a in I for b in I): return False
    return all(_in(mul(r,i), I) for r in R for i in I)

# Formula: I is a right ideal iff I·R ⊆ I
def IsRightIdeal(I, R_elements, add, mul, add_identity):
    I=list(I); R=list(R_elements)
    pairs=_inverses_map(R, add, add_identity)
    if pairs is None: return False
    if not all(_in(add(a, _get_inv(b,pairs)), I) for a in I for b in I): return False
    return all(_in(mul(i,r), I) for r in R for i in I)

# Returns True if I is a two-sided ideal
def IsIdeal(I, R_elements, add, mul, add_identity):
    return (IsLeftIdeal(I,R_elements,add,mul,add_identity) and
            IsRightIdeal(I,R_elements,add,mul,add_identity))

# Formula: ⟨a⟩ = {r·a | r ∈ R} — principal ideal generated by a (commutative ring)
def PrincipalIdeal(a, R_elements, mul):
    return sorted(_dedup([mul(r,a) for r in R_elements]))

# Formula: R/I — quotient ring (elements are cosets a+I)
def QuotientRing(R_elements, I, add, mul, add_identity):
    if not IsIdeal(I,R_elements,add,mul,add_identity): return None,None,None
    R=list(R_elements); cosets=[]
    for r in R:
        coset=_dedup([add(r,i) for i in I])
        if not any(set(map(str,coset))==set(map(str,c)) for c in cosets):
            cosets.append(coset)
    def coset_idx(x):
        for i,c in enumerate(cosets):
            if _in(x,c): return i
        return -1
    return cosets, lambda i,j:coset_idx(add(cosets[i][0],cosets[j][0])), \
                   lambda i,j:coset_idx(mul(cosets[i][0],cosets[j][0]))

# Formula: I is prime iff ab ∈ I → a ∈ I or b ∈ I
def IsPrimeIdeal(I, R_elements, mul):
    I_set=list(I); R=list(R_elements)
    for a in R:
        for b in R:
            if _in(mul(a,b),I_set) and not _in(a,I_set) and not _in(b,I_set):
                return False
    return True

# Formula: I is maximal iff R/I is a field (no proper ideals between I and R)
def IsMaximalIdeal(I, R_elements, add, mul, add_identity):
    cosets,add_op,mul_op=QuotientRing(R_elements,I,add,mul,add_identity)
    if cosets is None: return False
    n=len(cosets); zero_idx=next((i for i,c in enumerate(cosets) if _in(add_identity,c)),None)
    nonzero=[i for i in range(n) if i!=zero_idx]
    return all(mul_op(a,b)!=zero_idx for a in nonzero for b in nonzero)

