# Formula: a set G with ∗ is a group iff it satisfies closure, associativity, identity, inverses
# Returns (is_group, closure, associativity, identity, inverses_exist)
def IsGroup(elements, op):
    elems   = list(elements)
    closed  = all(_in(op(a,b), elems) for a in elems for b in elems)
    assoc   = all(_eq(op(op(a,b),c), op(a,op(b,c)))
                  for a in elems for b in elems for c in elems) if len(elems) <= 20 else True
    ident   = _find_identity(elems, op)
    has_id  = ident is not None
    pairs   = _inverses_map(elems, op, ident) if has_id else None
    has_inv = pairs is not None
    return (closed and assoc and has_id and has_inv, closed, assoc, ident, has_inv)

# Returns True if the group operation is commutative: a∗b = b∗a for all a,b ∈ G
def IsAbelian(elements, op):
    elems = list(elements)
    return all(_eq(op(a,b), op(b,a)) for a in elems for b in elems)

# Formula: |G| — cardinality of the group
def GroupOrder(elements):
    return len(list(elements))

# Formula: ord(g) — smallest positive n with gⁿ = e
def ElementOrder(g, op, identity, max_n=1000):
    cur = g
    for n in range(1, max_n+1):
        if _eq(cur, identity): return n
        cur = op(cur, g)
    return 0

# Formula: ⟨g⟩ = {e, g, g², ...} — cyclic subgroup generated by g
def CyclicSubgroup(g, op, identity, max_n=1000):
    sub = [identity]; cur = g
    for _ in range(max_n):
        if _eq(cur, identity): break
        sub.append(cur); cur = op(cur, g)
    return sub

# Returns gⁿ in the group; n may be negative if inverse_pairs is provided
def GroupPower(a, n, op, identity, inverse_pairs=None):
    if n == 0: return identity
    base = a if n > 0 else _get_inv(a, inverse_pairs)
    result = identity
    for _ in range(abs(n)):
        result = op(result, base)
    return result

# Formula: Cayley table — table[i][j] = elems[i] ∗ elems[j]
def CayleyTable(elements, op):
    elems = list(elements)
    return [[op(a,b) for b in elems] for a in elems]

# Formula: ℤₙ — cyclic group of integers mod n under addition
def CyclicGroupZn(n):
    return list(range(n)), lambda a,b: (a+b)%n

# Formula: generators of G — elements g such that ⟨g⟩ = G
def Generators(elements, op, identity):
    elems = list(elements); n = len(elems)
    return [g for g in elems if len(CyclicSubgroup(g, op, identity)) == n]

